/**
 * 加算メソッド
 * @param x - 数値
 * @param y - 数値
 * @returns - 足した値
 */
export const add = (x: number, y: number): number => {
  return x + y;
};

/**
 * 減算メソッド
 * @param x - 数値
 * @param y - 数値
 * @returns - 引いた値
 */
export const minus = (x: number, y: number): number => {
  return x - y;
};

/**
 * 乗算メソッド
 * @param x - 数値
 * @param y - 数値
 * @returns - 乗じた値
 */
export const multiply = (x: number, y: number): number => {
  return x * y;
};
